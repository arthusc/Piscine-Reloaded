int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 0)
	{
		return (0);
	}
	while (i * i < nb)
	{
		i++;
	}
	if (i % 1 == 0 && i * i == nb)
		return (i);
	else
		return (0);
}
